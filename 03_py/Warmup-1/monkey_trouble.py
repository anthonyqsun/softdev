def monkey_trouble(a_smile, b_smile):
  return a_smile == b_smile
print("monkey_trouble(True,True) expected True: " + str(monkey_trouble(True,True)))
print("monkey_trouble(False,False) expected True: " + str(monkey_trouble(False,False)))
print("monkey_trouble(True,False) expected False: " + str(monkey_trouble(True,False)))

